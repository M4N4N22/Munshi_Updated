def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    datasets = {
        "onboard_vendor.json": _build_onboard_vendor(),
        "onboard_worker.json": _build_onboard_worker(),
        "inventory_create.json": _build_inventory_create(),
        "inventory_status.json": _build_inventory_status(),
    }
    for name, rows in datasets.items():
        if len(rows) < 100:
            raise SystemExit(f"{name} has only {len(rows)} examples")
        path = OUT / name
        path.write_text(json.dumps(rows, indent=2, ensure_ascii=False), encoding="utf-8")
        print(f"Wrote {path} ({len(rows)} examples)")


if __name__ == "__main__":
    main()
